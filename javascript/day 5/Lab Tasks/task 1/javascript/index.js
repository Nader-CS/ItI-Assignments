let text = document.getElementById("PAR");

function ChangeFont(font) {
  text.style.fontFamily = font;
}
function ChangeAlign(align) {
  text.style.textAlign = align;
}

function ChangeHeight(height) {
  text.style.lineHeight = height;
}
function ChangeLSpace(spacing) {
  text.style.letterSpacing = spacing;
}
function ChangeIndent(indent) {
  text.style.textIndent = indent;
}
function ChangeTransform(transform) {
  text.style.textTransform = transform;
}
function ChangeDecorate(decoration) {
  text.style.textDecoration = decoration;
}
function ChangeBorder(borderStyle) {
  text.style.borderStyle = borderStyle;
}
function ChangeBorderColor(borderColor) {
  text.style.borderColor = borderColor;
}
